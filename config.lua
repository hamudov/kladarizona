-- ============================================
-- КЛАВИШИ И УПРАВЛЕНИЕ
-- ============================================

--[[
    В главном окне (/klua):
    [ 1 ] - Добавить точку из текущей позиции
    [ 2 ] - Добавить по вручную введенным координатам
    [ 3 ] - Удалить точку (выбрать из списка)
    [ 4 ] - Выбрать точку на карте (два клика)
    [ 5 ] - Сохранить все точки в файл
    [ ESC ] - Закрыть окно
    
    При вводе названия точки:
    [ ENTER ] - Подтвердить и добавить точку
    [ ESC ] - Отмена
    [ BACKSPACE ] - Удалить последний символ
    [ Буквы/Цифры ] - Ввести текст
    
    При вводе координат:
    [ TAB ] - Переключаться между полями (X → Y → Z → Name)
    [ 0-9 ] - Ввести цифры
    [ - ] - Добавить минус для отрицательной координаты
    [ BACKSPACE ] - Удалить последнее значение
    [ ENTER ] - Добавить точку
    [ ESC ] - Отмена
]]

