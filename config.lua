--[[
    РЕЖИМ 1: ДОБАВИТЬ ИЗ ТЕКУЩЕЙ ПОЗИЦИИ
    - Команда: /klua add [название]
    - Берет координаты игрока (X, Y, Z)
    - Сохраняет точку с указанным названием
    
    Пример: /klua add Золотая пещера
    
    РЕЖИМ 2: ДОБАВИТЬ ПО КООРДИНАТАМ
    - Команда: /klua (нажать [ 2 ] в окне)
    - Ввести вручную координаты X, Y, Z
    - Переключаться между полями можно TAB
    - Вводить название точки
    - ENTER для сохранения
    
    Пример использования:
    1. /klua
    2. [ 2 ] - нажать кнопку
    3. Ввести: 2145 [TAB] -2045 [TAB] 13 [TAB] Клад у дерева [ENTER]
    
    РЕЖИМ 3: ВЫБРАТЬ НА КАРТЕ (ДВА КЛИКА)
    - Команда: /klua (нажать [ 4 ] в окне)
    - ПЕРВЫЙ ЛКМ на карту - фиксирует первую точку
    - ВТОРОЙ ЛКМ на карту - подтверждает выбор
    - Ввести название
    - ENTER для сохранения
    
    Пример использования:
    1. /klua
    2. [ 4 ] - нажать кнопку
    3. ЛКМ на карту (первый раз)
    4. ЛКМ на карту (второй раз)
    5. Ввести: Загадочное место [ENTER]
]]

